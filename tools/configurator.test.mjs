test("every CI target renders non-empty for linux/docker and windows/native", () => {
  const targets = Object.keys(load().window ? {} : {}); // placeholder; enumerate from schema-independent list
  const all = ["github","gitlab","azure","jenkins","bamboo","bitbucket","teamcity","buildkite","codebuild","circleci","travis","drone","semaphore","appveyor","codefresh"];
  for (const t of all) {
    const a = fresh(); a.__state.target = t; const la = out(a);
    assert.ok(la && la.length > 20, `${t} linux/docker non-empty`);
    const b = fresh(); b.__state.target = t; b.__state.os = "windows"; b.__state.runtime = "native"; const lb = out(b);
    assert.ok(lb && lb.length > 20, `${t} windows/native non-empty`);
  }
});

/* ---------- flags & safety ---------- */
test("project name and team flags appear", () => {
  const c = fresh(); c.__state.projectName = "myapp"; c.__state.sast.team = "CxServer/SP";
  const o = out(c);
  assert.match(o, /--project-name myapp/);
  assert.match(o, /--sast-team CxServer\/SP/);
});

test("client-credentials wires the secret by ENV NAME only (never a value)", () => {
  const c = fresh(); c.__state.target = "gitlab"; c.__state.auth = "client-creds";
  Object.assign(c.__state.authEnv, { clientId:"cxone-ci", clientSecretEnv:"CX_CLIENT_SECRET", baseUri:"https://x.ast.checkmarx.net", baseAuthUri:"https://x.iam.checkmarx.net", tenant:"acme" });
  const o = out(c);
  assert.match(o, /--cx-client-id cxone-ci/);
  assert.match(o, /--cx-client-secret-env CX_CLIENT_SECRET/);
  assert.ok(!/--cx-client-secret\s/.test(o), "no secret VALUE flag");
});

test("secrets-total threshold and =-bound passthrough", () => {
  const c = fresh();
  c.__state.thresholds = [{engine:"secrets-total",sev:"high",limit:"1"}];
  c.__state.passthrough = [{engine:"sast",value:"-ReportPDF=report.pdf"}];
  const o = out(c);
  assert.match(o, /--threshold (")?secrets-total=1(")?/);
  assert.match(o, /--sast-arg=-ReportPDF=report\.pdf/);
});

test("no secret VALUES ever appear in output", () => {
  for (const t of ["local","github","gitlab","jenkins","azure"]) {
    const c = fresh(); c.__state.target = t;
    assert.ok(!/eyJ|REDACTED|password=\S{6}/.test(out(c)), `${t} leaks no secret value`);
  }
});

test("selecting no engines warns that --scanners is required", () => {
  const c = fresh(); c.__state.scanners = {sast:false,sca:false,kics:false,secrets:false,containers:false};
  assert.match(notes(c).toLowerCase(), /at least one engine/);
});

test("--async with --threshold warns (incompatible)", () => {
  const c = fresh(); c.__state.async = true;
  assert.match(notes(c).toLowerCase(), /async/);
});
